int config_mphy_reg(struct ufs_hba *hba)
{
	uint32_t i;
	uint32_t set;
	int err = 0;

	mutex_lock(&hba->uic_cmd_mutex);
	set = ufshcd_readl(hba, REG_INTERRUPT_ENABLE);
	ufshcd_writel(hba, set & ~UIC_COMMAND_COMPL, REG_INTERRUPT_ENABLE);

	for (i = 0; i < ARRAY_SIZE(phy_value); i++) {
		err =
		    ufs_kirin_polling_mphy_write(hba, PHY_START + i,
						 phy_value[i]);
		if (err)
			goto out;
	}
out:
	ufshcd_writel(hba, set, REG_INTERRUPT_ENABLE);
	mutex_unlock(&hba->uic_cmd_mutex);
	return err;
}

/*lint -e648 -e845*/
int ufs_update_hc_fw(struct ufs_hba *hba)
{
	int retry = 100;
	int err = 0;
	struct ufs_kirin_host *host = hba->priv;

	if (unlikely(hba->host->is_emulator))
		return 0;

	while ((0 == (UFS_SYS_PHY_SRAM_INIT_DONE &
		      ufs_sys_ctrl_readl(host, UFS_SYS_PHY_SRAM_MEM_CTRL_S)))
	       && retry--) {
		mdelay(1);
	}

	if (retry < 0)
		pr_info("%s, wait PHY_SRAM_INIT_DONE timeout\n", __func__);

	err = config_mphy_reg(hba);

	/* enable sram_ext_ld_done */
	ufs_sys_ctrl_set_bits(host, SRAM_EXT_LD_DONE_BIT,
			      UFS_SYS_PHY_SRAM_MEM_CTRL_S);

	return err;
}
