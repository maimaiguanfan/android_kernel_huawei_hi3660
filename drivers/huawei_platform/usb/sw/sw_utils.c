static uint16_t crc_ccitt_fast(uint16_t crc_value, u8 *buffer, u32 length)
{
	u8 *data_buf = buffer;
	u32 data_len = length;
	uint16_t crc = crc_value;

	while (data_len--)
		crc = crc_ccitt_tab[(crc >> BITS_NUM_PER_BYTE ^ *data_buf++) &
			0xFF] ^ (crc << BITS_NUM_PER_BYTE);

	return crc;
}

/*
 * sw protocol packet struct
 * byte0  byte1  byte2    byte3  byte4    XX  XX  XX  XX
 *  ID    LEN    DevID    crc_H  crc_L   [ data (LEN-5)  ]
 */
int sw_check_recv_data(const u8 *data, int count)
{
	uint16_t crc;
	uint16_t crc_data;

	if ((data == NULL) || (count < KB_PROTO_HEAD_LEN))
		return -1;

	crc_data = (data[KB_CRC_HIGH_BYTE] << BITS_NUM_PER_BYTE) +
		data[KB_CRC_LOW_BYTE];

	crc = crc_ccitt_fast(0, (u8 *)(data + KB_PROTO_HEAD_LEN),
		count - KB_PROTO_HEAD_LEN);
	SW_PRINT_DBG("crc_ccitt %x %x\n", crc, crc_data);
	if (crc != crc_data)
		return -1;

	return 0;
}

int hkadc_detect_value(int adc)
{
	int retry_times = 3;
	int cnt = 0;
	int adc_val;

	while (cnt++ < retry_times) {
		adc_val = hisi_adc_get_value(adc);

		if (adc_val < 0) {
			SW_PRINT_ERR(
				"sw detect adc read fail, retry_cnt = %d\n",
				cnt);
		} else {
			SW_PRINT_INFO("sw detect adc read success, adc = %d\n",
				adc_val);
			return adc_val;
		}
	}

	SW_PRINT_ERR("kb detect read retry maxtimes, error\n");

	return -1;
}
